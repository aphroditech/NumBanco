export const initializeKenoControls = async () => {
  try {
    const count = await KenoControl.countDocuments(); // ✅ FIXED

    if (count === 0) {
      const documents = [];

      for (let numbersLength = 1; numbersLength <= 10; numbersLength++) {
        const doc = {
          numbersLength,
          low: [],
          classic: [],
          medium: [],
          high: [],
        };

        for (const riskType of ['low', 'classic', 'medium', 'high']) {
          const payouts = KENO_PAYOUT_BY_RISK[riskType][numbersLength];

          doc[riskType] = payouts.map((multiplier, index) => ({
            winLength: index,
            probability: 1,
            multiplier: multiplier,
          }));
        }

        documents.push(doc);
      }

      await KenoControl.insertMany(documents);
      console.log('✅ KenoControls initialized');
    } else {
      console.log('✅ KenoControls already exist');
    }
  } catch (error) {
    console.error('❌ Error initializing KenoControls:', error);
  }
};